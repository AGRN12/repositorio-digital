<h2>Ingeniería de Sistemas</h2>
<p>La Ingeniería de Sistemas se enfoca en el diseño y gestión de sistemas complejos de información. Los ingenieros de sistemas desarrollan y mantienen sistemas
 informáticos integrados que soportan las operaciones y procesos de una organización. Esto incluye el análisis de requerimientos, diseño de software, gestión de
  bases de datos, redes de comunicación y seguridad informática. Su objetivo es asegurar que los sistemas de información sean eficientes, fiables y capaces de evolucionar con las necesidades de la organización.</p>
