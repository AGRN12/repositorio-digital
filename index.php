<h2>Ingeniería Industrial</h2>
<p>La Ingeniería Industrial se dedica a la optimización de procesos y sistemas de producción y manufactura. Los ingenieros industriales buscan mejorar la eficiencia, productividad y calidad en las operaciones de una empresa. Esto incluye el diseño de sistemas
de producción, la gestión de la cadena de suministro, la ergonomía, la logística, y la implementación de metodologías como Lean Manufacturing y Six Sigma. Su objetivo es minimizar costos y maximizar el rendimiento y la satisfacción del cliente.</p>
