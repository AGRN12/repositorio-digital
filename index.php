<h1>HISTORIA DE ICEL</h1>
<br>
<p>Universidad ICEL (International College for Experienced Learning) es una institución educativa particular que en sus inicios, al encontrar claros referentes de una población en busca de opciones que le permitieran su superación y para la cual existían pocas oportunidades de acceso, se sintió comprometida a impulsar la creación de un proyecto educativo alternativo, encontrando en el bachillerato tecnológico una opción práctica y viable, de modo que en el año de 1990 inauguró dos campi dedicados a esta modalidad educativa: Lindavista y Londres (hoy Zona Rosa).
En México, país donde la población crece aceleradamente y con ella, sus necesidades, la magnitud de la demanda existente motivó a esta institución a acrecentar su oferta educativa durante 1991, con la creación de los campi Acueducto y Atizapán, los cuales sumaron a sus programas de estudio los de secundaria técnica, permitiendo así a la población de estas zonas tener más oportunidades de instrucción y habilidades para el trabajo.
Asimismo, en 1994 iniciaron actividades los campi Ermita, Zaragoza y La Villa; al mismo tiempo se consolidó la impartición de estudios superiores en los campi La Villa, Zona Rosa y Ermita.
<br> 
MISIÓN:
Generar alternativas de superación al mayor número de estudiantes, proporcionándoles acceso a una educación de calidad que les facilite su integración al campo laboral.
<br> 
VISIÓN:
Ser una institución educativa particular de alta calidad con una amplia oferta de programas académicos en todos los niveles y las diferentes áreas del conocimiento, formadora de profesionales que resuelvan e impulsen el desarrollo del país.</p>
