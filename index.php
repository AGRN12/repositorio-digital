  <h2>Ingeniería Robótica</h2>
<p>La Ingeniería Robótica involucra el diseño, construcción y operación de robots. Los ingenieros robóticos trabajan en el desarrollo de sistemas automatizados que pueden realizar tareas de manera autónoma o semi-autónoma. Esto abarca desde robots industriales
 que ensamblan productos en fábricas hasta robots médicos que asisten en cirugías. La disciplina combina conocimientos de mecánica, electrónica, informática y control, y abarca áreas como la inteligencia artificial, la visión por computadora y la mecánica de precisión.</p>
<br>
